# 숫자로 이루어진 문자열을 정수로 변환할 때는 int()를 사용
n1 = 10
n2 = '34'
print(n1 + int(n2)) # 결과: 44
print('=' * 35)

# 실수로 이루어진 문자열을 실수로 변환할 때는 float()를 사용.
s = '2.34'
print(10 + float(s)) # 결과: 12.34
# 여기서 float을 int로 바꾸면 에러남!
print(10 + float('3.14e-2')) # 결과: 10.0314

