# 반올림 함수
# 반올림을 할 때는 round()를 사용합니다.


print(round(4.78)) # 결과: 5 -> 소수 첫번째 자리에서 반올림 한 것을 알 수 있다.
# 만약 소숫점 둘째자리에서 반올림 하고 싶으면?
